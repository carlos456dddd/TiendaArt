public void MostrarDatos(){
    String sql = "SELECT * FROM productos";
try{
            con = cn.getConnection();
            st = con.createStatement();
            rs = st.executeQuery(sql);
            Object[] tienda = new Object[4];
            modelo = (DefaultTableModel)TablaPro.getModel();
            
            while(rs.next()){
                tienda[0] = rs.getInt("Id");
                tienda[1] = rs.getString("Codigo");
                tienda[2] = rs.getString("Nombre");
                tienda[3] = rs.getFloat("Precio");
                modelo.addRow(tienda);
            }
            TablaPro.setModel(modelo);
        } catch(Exception e){
        System.out.println(e);
            
        }

    
    
    
    
}
    
    
    
    
    
    
    
