    private void TablaProMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaProMouseClicked
      int row = TablaPro.getSelectedRow();
        
        if (row == -1){
            JOptionPane.showMessageDialog(null, "No se selecciono ninguna");
        } else{
            id = Integer.parseInt((String) TablaPro.getValueAt(row, 0).toString());
            String codigo = (String) TablaPro.getValueAt(row, 1);
            String nombre = (String) TablaPro.getValueAt(row,2);
            float precio = Float.parseFloat((String) TablaPro.getValueAt(row, 3).toString());
            cid.setText(""+id);
            ccodigo.setText(codigo);
            cnombre.setText(""+nombre);
            cprecio.setText(""+precio);
        }

    }//GEN-LAST:event_TablaProMouseClicked

